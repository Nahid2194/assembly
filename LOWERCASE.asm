INCLUDE EMU8086.INC

.MODEL SMALL
.STACK 100H
.CODE

MAIN PROC
    PRINT 'ENTER LOWERCASE CHARACTER: '
    MOV AH,1
    INT 21H
    MOV BL,AL
    SUB BL,32
    
    PRINTN ' ' 
    PRINT 'CONVERTED UPPERCASE FROM  LOWERCASE: ' 
    MOV AH,2
    MOV DL,BL
    INT 21H
    
   
END MAIN
    



